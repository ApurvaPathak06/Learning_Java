package ex_03_Operators;

public class Lab020_Arithmetic_Operators {
    public static void main(String[] args) {
        /*            Arithmetic operators
        +(Addition),
        -(Subtraction),
        *(Multiplication),
        /(division),
        %(Modulus)          */

        int a=20;
        int b=3;
        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
    }
}

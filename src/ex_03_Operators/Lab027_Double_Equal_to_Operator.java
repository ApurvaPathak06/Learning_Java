package ex_03_Operators;

public class Lab027_Double_Equal_to_Operator {
    public static void main(String[] args) {
        //== is used to compare values

        System.out.println(10==10);
        System.out.println(10<10);
        System.out.println(10>=10);
        System.out.println(10==30);

        System.out.println(!(10<40));

    }
}

package ex_02_Literals;

public class Lab018_Non_Primitive_Literals {
    public static void main(String[] args) {
        //Primitive data types  -> Defined by Java guys
        //Max,Min,Size defined
        int i=10;
        byte b=5;

        //Non-primitive data type(Also called as Reference data type) -> Defined by users
        //No Max,No Min,no size defined
        //strings,arrays,classes,enum,etc
        int[] array_of_items=new int[5];
    }
}

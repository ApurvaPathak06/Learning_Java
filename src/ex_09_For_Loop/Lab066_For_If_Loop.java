package ex_09_For_Loop;

public class Lab066_For_If_Loop {
    public static void main(String[] args) {
        for (int g=0;g<18;g++){
            if (g<17){
                System.out.println("Not eligible to Vote..");
            }else {
                System.out.println("You can vote now...Yaayyy!!!");
            }
        }
    }
}
